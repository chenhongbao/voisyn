            CONTENT VARCHAR(20) NOT NULL PRIMARY KEY,
            FREQUENCY BIGINT UNSIGNED DEFAULT 0 COMMENT 'HOW MANY WORDS FALL INTO THIS MAJOR',
            NUMB_1 INT,
            NUMB_2 INT,
            NUMB_3 INT,
            NUMB_4 INT,
            RANK_1 INT,
            RANK_2 INT,
            RANK_3 INT,
            RANK_4 INT,
            RATE_1 INT,
            RATE_2 INT,
            RATE_3 INT,
            RATE_4 INT,
            INDEX CONTENT USING HASH (CONTENT)
        ) ENGINE=MyISAM DEFAULT CHARACTER SET=utf8 COLLATE utf8_bin COMMENT 'LIST OF MAJOR INFO FOR WORDS';
CREATE TABLE IF NOT EXISTS
        WORD_MINOR_INFO (
            CONTENT VARCHAR(20) NOT NULL,
            MAJOR VARCHAR(20) NOT NULL,
            FREQUENCY BIGINT UNSIGNED DEFAULT 0 COMMENT 'HOW MANY WORDS FALL INTO THIS MAJOR',
            NUMB_1 INT,
            NUMB_2 INT,
            NUMB_3 INT,
            NUMB_4 INT,
            RANK_1 INT,
            RANK_2 INT,
            RANK_3 INT,
            RANK_4 INT,
            RATE_1 INT,
            RATE_2 INT,
            RATE_3 INT,
            RATE_4 INT,
            INDEX CONTENT USING HASH (CONTENT),
            INDEX MAJOR USING HASH (MAJOR),
            INDEX CONTENT_BTREE USING BTREE (CONTENT),
            INDEX MAJOR_BTREE USING BTREE (MAJOR),
            INDEX CONJUCTION USING BTREE (MAJOR, CONTENT)
        ) ENGINE=MyISAM DEFAULT CHARACTER SET=utf8 COLLATE utf8_bin COMMENT 'LIST OF MINOR INFO FOR WORDS';


CREATE TABLE IF NOT EXISTS
        TEXT_WORD_SEGMENT_LIST_LEVEL_0 (
            ID BIGINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
            CONTENT VARCHAR(20) NOT NULL,
            HASH_1 BIGINT UNSIGNED NOT NULL, -- HASH VALUE FOR THE CONTENT
            HASH_2 BIGINT UNSIGNED,
            HASH_3 BIGINT UNSIGNED,
            HASH_4 BIGINT UNSIGNED, -- HASH VALUE FOR THE WHOLE PASSEGE, COMPUTED WITH ALL THE TEXT 
            SERIAL BIGINT UNSIGNED NOT NULL,
            MAJOR VARCHAR(20) NOT NULL,
            MINOR VARCHAR(20) NOT NULL,
            TITLE VARCHAR(20) NOT NULL,
            WHEN_INSERT DATETIME,
            WHEN_UPDATE DATETIME NOT NULL,
            INDEX CONTENT USING BTREE (CONTENT),
            INDEX MAJOR USING BTREE (MAJOR),
            INDEX MINOR USING BTREE (MINOR),
            INDEX TITLE USING BTREE (TITLE)
        )ENGINE=MyISAM DEFAULT CHARACTER SET=utf8 COLLATE utf8_bin COMMENT 'LIST OF WORDS FROM ARTICIFIAL SEGMENTATION';
        
ALTER TABLE TEXT_WORD_SEGMENT_LIST_LEVEL_0 MODIFY HASH_4 BIGINT UNSIGNED COMMENT 'HASH VALUE FOR THE WHOLE PASSEGE, AS IDENTIFIRE';        
CREATE UNIQUE INDEX CONTENT_SERIAL_HASH
        USING BTREE  
        ON  TEXT_WORD_SEGMENT_LIST_LEVEL_0
        (CONTENT, SERIAL, HASH_4);

CREATE TABLE IF NOT EXISTS TRASH (
