    CHARACTER_LIST_LEVEL_0(
            UNICODE         INT     NOT NULL    COMMENT 'UNICODE OF THE CHARACTER, INTEGER NUMBER IS PORTABLE FOR ALL DBs',
            LITERAL         VARCHAR(1)      COMMENT 'CHARACTER THE SAME AS THE UNICODE_1 REPRESENTATION',
            FREQUENCY       BIGINT  DEFAULT 0   COMMENT 'FREQUENCY_1 FOR UNICODE_1, FREQUENCY OF THE CHARACTER IN CORPUS',
            STRING          VARCHAR(100)    COMMENT 'TEXT FOR EXTRA CORPUS',
            RATE_1         DOUBLE(15,12)   DEFAULT 0.0,
            RATE_2         DOUBLE(15,12)   DEFAULT 0.0,
            RATE_3         DOUBLE(15,12)   DEFAULT 0.0,
            NUMB_1         INT,
            NUMB_2         INT,
            NUMB_3         INT,
            SPELL_1        VARCHAR(20),
            SPELL_2        VARCHAR(20),
            SPELL_3        VARCHAR(20),
            SPELL_4        VARCHAR(20),
            SPELL_5        VARCHAR(20),
            SPELL_6        VARCHAR(20),
            WHEN_INSERT     DATETIME,
            WHEN_UPDATE     DATETIME,
            PRIMARY KEY(UNICODE),
            UNIQUE INDEX UNIC_FREQ USING HASH(UNICODE, FREQUENCY),
            UNIQUE INDEX UNIC USING HASH (UNICODE),
            INDEX LITR_FREQ USING HASH(LITERAL, FREQUENCY),
            INDEX LITR USING HASH (LITERAL),
            INDEX FREQ USING BTREE (FREQUENCY),
            INDEX RATE USING BTREE (RATE_1, RATE_2, RATE_3) 
            ) ENGINE=MyISAM DEFAULT CHARACTER SET=utf8 COLLATE utf8_bin COMMENT 'BASIC CHARACTER SET FOR NEARLY ALL CHARACTERS IN CHINESE';

